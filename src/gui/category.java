    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
int ri = tab.getSelectedRow(); // Correct method and variable
if (ri != -1) { // Check if a row is selected
    String category = (String) tab.getValueAt(ri, 1); // Correct casting
    
    // Show confirmation dialog
    int response = JOptionPane.showConfirmDialog(null, 
        "Are you sure you want to delete the selected category?", 
        "Confirm Deletion", 
        JOptionPane.YES_NO_OPTION, 
        JOptionPane.WARNING_MESSAGE);

    if (response == JOptionPane.YES_OPTION) {
        try {
            Connection c = DriverManager.getConnection(
                    "jdbc:mysql://localhost:3306/spendingdb?useSSL=false", "root", "Subham@7643");
            Statement s = c.createStatement();
            // Correct SQL statement for deletion
            s.executeUpdate("DELETE FROM category_details WHERE category = '" + category + "'");
            JOptionPane.showMessageDialog(null, "Category deleted successfully!");
            getEntries(); // Refresh the table
            c.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    } else {
        JOptionPane.showMessageDialog(null, "Category deletion canceled.");
    }
} else {
    JOptionPane.showMessageDialog(null, "No category selected.");
}

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
       try {
    String category = t.getText().trim(); // Trim whitespace
    if (category.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Category cannot be empty!");
        return;
    }

    // Establish the connection
    try (Connection c = DriverManager.getConnection(
            "jdbc:mysql://localhost:3306/spendingdb?useSSL=false", "root", "Subham@7643")) {

        // Use a PreparedStatement to avoid SQL injection
        String sql = "INSERT INTO category_details VALUES (?)";
        try (PreparedStatement ps = c.prepareStatement(sql)) {
            ps.setString(1, category);
            ps.executeUpdate();
        }

        JOptionPane.showMessageDialog(null, "Category added successfully!");
        getEntries(); // Refresh or update the UI
    }

} catch (SQLIntegrityConstraintViolationException ex) {
    JOptionPane.showMessageDialog(null, "Category already exists.");
} catch (SQLException ex) {
    JOptionPane.showMessageDialog(null, "Database error: " + ex.getMessage());
} catch (Exception ex) {
    JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
}

    }//GEN-LAST:event_jButton2ActionPerformed

