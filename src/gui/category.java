   void getEntries() {
    try {
        // Connection setup
        Connection c = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/spendingdb?useSSL=false", "root", "Subham@7643");
        Statement s = c.createStatement();
        ResultSet rs = s.executeQuery("SELECT * FROM category_details");

        // Use the correct table variable
        DefaultTableModel dtm = (DefaultTableModel) tab.getModel(); // Corrected variable name
        dtm.setRowCount(0); // Clear existing rows

        int sno = 0;
        while (rs.next()) {
            String category = rs.getString("category");

            // Create a new row
            Vector<Object> row = new Vector<>();
            row.add(++sno);       // Serial number
            row.add(category);    // Category from ResultSet

            // Add the row to the table model
            dtm.addRow(row);
        }

        c.close();
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(null, ex);
    }
}


