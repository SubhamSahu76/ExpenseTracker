    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int ri = table.getSelectedRow();
if (ri != -1) {  // Ensure a row is selected
    int response = JOptionPane.showConfirmDialog(
        null, 
        "Are you sure you want to delete the selected category?", 
        "Confirm Deletion", 
        JOptionPane.YES_NO_OPTION, 
        JOptionPane.WARNING_MESSAGE
    );

    if (response == JOptionPane.YES_OPTION) {  // Confirm deletion
        int id = (int) table.getValueAt(ri, 0);  // Get the ID from the selected row
        try {
            db.DbConnect.st.executeUpdate("delete from expense where sid=" + id);
            JOptionPane.showMessageDialog(null, "DELETED");
            getEntries();  // Refresh the table after deletion
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);  // Handle any exceptions
        }
    }
} else {
    JOptionPane.showMessageDialog(null, "Please select a row to delete.");
}

    }//GEN-LAST:event_jButton3ActionPerformed

