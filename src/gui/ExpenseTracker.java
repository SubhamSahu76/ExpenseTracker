  private void displayCategory() {
    try {
        // Establish a connection to the database
        category.removeAllItems();
        Connection c = DriverManager.getConnection(
            "jdbc:mysql://localhost:3306/spendingdb?useSSL=false", "root", "Subham@7643");
        
        // Create a Statement object
        Statement s = c.createStatement();
        ResultSet rs = s.executeQuery("select* from category_details");
        
        // Process the ResultSet
        while (rs.next()) {
            category.addItem(rs.getString("category"));
        }
        
        // Close the ResultSet and Statement
        rs.close();
        s.close();
        c.close();
        
    } catch (Exception ex) {
        // Display an error message if an exception occurs
        JOptionPane.showMessageDialog(null, ex);
    }
}
  private void getEntries(){
      try{
          javax.swing.table.DefaultTableModel dtm=(javax.swing.table.DefaultTableModel)table.getModel();
          int rc=dtm.getRowCount();
          while(rc--!=0){
              dtm.removeRow(0);
          }
          java.time.LocalDate cd=java.time.LocalDate.now();
          java.time.LocalDate bd=cd.minusDays(28);
          ResultSet rs=db.DbConnect.st.executeQuery(
          "select * from expense where date<='"+cd+"' and date>='"+bd+"'");
          int total=0;
          while(rs.next()){
              int t=rs.getInt("amount"); 
              total+=t;
              Object o[]={rs.getInt("sid"),rs.getString("category"),rs.getDate("date"),rs.getInt("amount"),t};
              dtm.addRow(o);
          }
          totalAmount.setText(total+"");
      }catch(Exception ex){
          JOptionPane.showMessageDialog(null, ex);
      
  }
  }


